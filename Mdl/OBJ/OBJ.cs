using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace LakiTool.Mdl.OBJ
{
    class OBJ
    {
        public List<Mdl.OBJ.OBJFile.o> objects = new List<OBJFile.o>();

        public string[] val()
        {
            List<string> objLines = new List<string>();
            objLines.Add("#Generated by LakiTool");
            objLines.Add("#https://github.com/robinerd123/LakiTool");
            objLines.Add(null);
            int objectCount = 0;
            foreach (OBJFile.o objectelem in objects)
            {
                objectCount++;
                objLines.Add("#Object " + objectCount.ToString());
                objLines.Add(null);
                objLines.AddRange(objectelem.val());
                objLines.Add(null);
            }
            return objLines.ToArray();
        }
    }
}
